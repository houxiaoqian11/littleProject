package com.sumavision.tvpay.connector.vo;

import com.thoughtworks.xstream.XStream;
import com.thoughtworks.xstream.annotations.XStreamAlias;
import com.thoughtworks.xstream.annotations.XStreamAsAttribute;

@XStreamAlias("head")
public class Head {	
	@XStreamAlias("version")
	private String version;
	
	@XStreamAlias("merchantId")
	private String merchantId;
	
	@XStreamAlias("msgType")
	private String msgType;
	
	@XStreamAlias("tranCode")
	private String tranCode;
	
	@XStreamAlias("reqMsgId")
	private String reqMsgId;
	
	@XStreamAlias("payMsgId")
	private String payMsgId;
	
	@XStreamAlias("reqDate")
	private String reqDate;
	
	@XStreamAlias("respDate")
	private String respDate;
	
	@XStreamAlias("respType")
	private String respType;
	
	@XStreamAlias("respCode")
	private String respCode;
	
	@XStreamAlias("respMsg")
	private String respMsg;

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getMerchantId() {
		return merchantId;
	}

	public void setMerchantId(String merchantId) {
		this.merchantId = merchantId;
	}

	public String getMsgType() {
		return msgType;
	}

	public void setMsgType(String msgType) {
		this.msgType = msgType;
	}

	public String getTranCode() {
		return tranCode;
	}

	public void setTranCode(String tranCode) {
		this.tranCode = tranCode;
	}

	public String getReqMsgId() {
		return reqMsgId;
	}

	public void setReqMsgId(String reqMsgId) {
		this.reqMsgId = reqMsgId;
	}

	public String getPayMsgId() {
		return payMsgId;
	}

	public void setPayMsgId(String payMsgId) {
		this.payMsgId = payMsgId;
	}

	public String getReqDate() {
		return reqDate;
	}

	public void setReqDate(String reqDate) {
		this.reqDate = reqDate;
	}

	public String getRespDate() {
		return respDate;
	}

	public void setRespDate(String respDate) {
		this.respDate = respDate;
	}

	public String getRespType() {
		return respType;
	}

	public void setRespType(String respType) {
		this.respType = respType;
	}

	public String getRespCode() {
		return respCode;
	}

	public void setRespCode(String respCode) {
		this.respCode = respCode;
	}

	public String getRespMsg() {
		return respMsg;
	}

	public void setRespMsg(String respMsg) {
		this.respMsg = respMsg;
	}

	
	
}
